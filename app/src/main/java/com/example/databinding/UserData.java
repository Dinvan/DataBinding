package com.example.databinding;

public class UserData {
    private String sName;
    private long mm;
    private long om;
    private String gr;

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public long getMm() {
        return mm;
    }

    public void setMm(long mm) {
        this.mm = mm;
    }

    public long getOm() {
        return om;
    }

    public void setOm(long om) {
        this.om = om;
    }

    public String getGr() {
        return gr;
    }

    public void setGr(String gr) {
        this.gr = gr;
    }
}
